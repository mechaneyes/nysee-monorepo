const API_URL = process.env.WORDPRESS_API_URL;

async function fetchAPI(query = "", { variables }: Record<string, any> = {}) {
  const headers = { "Content-Type": "application/json" };

  if (process.env.WORDPRESS_AUTH_REFRESH_TOKEN) {
    headers["Authorization"] =
      `Bearer ${process.env.WORDPRESS_AUTH_REFRESH_TOKEN}`;
    console.log("Auth token is set");
  } else {
    console.log("No auth token found");
  }

  console.log("API URL:", API_URL);
  console.log("Query:", query);
  console.log("Variables:", variables);

  // WPGraphQL Plugin must be enabled
  const res = await fetch(API_URL, {
    headers,
    method: "POST",
    body: JSON.stringify({
      query,
      variables,
    }),
  });

  const json = await res.json();
  if (json.errors) {
    console.error("GraphQL Errors:", json.errors);
    throw new Error("Failed to fetch API");
  }
  return json.data;
}

export async function getPreviewPost(id, idType = "DATABASE_ID") {
  const data = await fetchAPI(
    `
    query PreviewPost($id: ID!, $idType: PostIdType!) {
      post(id: $id, idType: $idType) {
        databaseId
        slug
        status
        title
        content
        isRevision
        isPreview
        preview {
          node {
            databaseId
            slug
            status
            content
            title
          }
        }
        revisions(first: 1) {
          edges {
            node {
              databaseId
              slug
              status
              content
              title
            }
          }
        }
      }
    }`,
    {
      variables: { id, idType },
    }
  );

  // If we have preview data, return that
  if (data?.post?.preview?.node) {
    return data.post.preview.node;
  }
  // If we have revision data, return that
  if (data?.post?.revisions?.edges?.[0]?.node) {
    return data.post.revisions.edges[0].node;
  }
  // Otherwise return the post itself
  return data?.post;
}

export async function getAllPostsWithSlug() {
  const data = await fetchAPI(`
    {
      posts(first: 10000) {
        edges {
          node {
            slug
          }
        }
      }
    }
  `);
  return data?.posts;
}

